// ZoneID make zoneid by country Name、province Name、city Name
func ZoneID(country, province, city string) (zoneid int64) {
	var (
		coid, prid, ciid int64
	)
	coid = aCountry[country]
	prid = aProvince[country+province]
	ciid = aCity[country+province+city]
	zoneid = coid<<22 | prid<<14 | ciid<<4
	return
}
